    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      Clase c=new Clase();
      String TEXTO;
      char TEXTO2 = 0; 
      
      TEXTO=String.valueOf(jTextField1.getText());
      TEXTO2=TEXTO.charAt(0);

       c.guionXLetra(TEXTO2,niveles);
       imprimir.setText(c.getCadena());
       
       if (imprimir.getText().equals(c.getPabra(niveles)))
           jButton4.setEnabled(true);
       else 
           jButton4.setEnabled(false);
       
       c.imgError(niveles, TEXTO);
       ImageIcon fot = new ImageIcon(getClass().getResource(c.getImgEr())); 
       Icon icono = new ImageIcon(fot.getImage().getScaledInstance(Imagen.getWidth(), Imagen.getHeight(), Image.SCALE_DEFAULT)); 
       Imagen.setIcon(icono); 
       
       if (c.getError()>=6){
        ImageIcon fot2 = new ImageIcon(getClass().getResource("/Ventana/e0.jpg")); 
        Icon icono2 = new ImageIcon(fot2.getImage().getScaledInstance(Imagen.getWidth(), Imagen.getHeight(), Image.SCALE_DEFAULT)); 
        Imagen.setIcon(icono2);
        err.setText("0");
        imprimir.setText(null);
        jTextField1.setText(null);
        niveles=0;
        jButton3.setEnabled(true);
        jButton1.setEnabled(false);}
    }//GEN-LAST:event_jButton1ActionPerformed

